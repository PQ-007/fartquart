// Sync the Obsidian vault's images/videos into public/ so they ship as static
// assets on any host (Vercel, Netlify, a plain Node server). Runs before dev
// and build. Incremental: only copies files that are new or newer than the copy.
//
// Also emits src/lib/resources-manifest.ts — a basename→path lookup used to
// resolve Obsidian `![[embed.png]]` references WITHOUT scanning content/resources
// at runtime, so that 195MB folder never has to be bundled into the server.

import { cpSync, existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs"
import { join, relative, sep } from "node:path"

const root = process.cwd()
const src = join(root, "content", "resources")
const dest = join(root, "public", "resources")
const manifestPath = join(root, "src", "lib", "resources-manifest.ts")

const writeManifest = (entries) => {
  const body = `// AUTO-GENERATED by scripts/copy-resources.mjs — do not edit.
// Maps lowercased basename and relative path → relative path under content/resources.
export const resourceManifest: Record<string, string> = ${JSON.stringify(entries, null, 2)}
`
  writeFileSync(manifestPath, body)
}

if (!existsSync(src)) {
  console.log("[copy-resources] no content/resources — writing empty manifest")
  writeManifest({})
  process.exit(0)
}

mkdirSync(dest, { recursive: true })

cpSync(src, dest, {
  recursive: true,
  force: true,
  filter: (from, to) => {
    const st = statSync(from)
    if (st.isDirectory()) return true
    try {
      return st.mtimeMs > statSync(to).mtimeMs
    } catch {
      return true // destination doesn't exist yet
    }
  },
})

// Build the manifest.
const manifest = {}
const walk = (dir) => {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) {
      walk(full)
      continue
    }
    const rel = relative(src, full).split(sep).join("/")
    manifest[rel.toLowerCase()] = rel
    const base = entry.name.toLowerCase()
    if (!(base in manifest)) manifest[base] = rel // first match wins for a basename
  }
}
walk(src)
writeManifest(manifest)

console.log(
  `[copy-resources] synced content/resources → public/resources (${Object.keys(manifest).length} manifest entries)`,
)
